public class KylehasAids {
    public static void main (String[] args) {
        
        int j = 0;
    
        for(int i = 6; i>0; i--){
            while(j<i-2){
                System.out.print(i);
                j++;
            }
            if(i==4){
                System.out.println(i);
            }
            System.out.println();
        }
    }
}